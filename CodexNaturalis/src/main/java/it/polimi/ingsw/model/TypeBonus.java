package it.polimi.ingsw.model;

/**
 * Enum representing different types of bonuses that can be obtained in the game.
 */
public enum TypeBonus {
	/**
	 * Bonus for hiding in the corner.
	 */
	HIDECORNER,

	/**
	 * Bonus for counting element Ink.
	 */
	COUNTELEMENT_I,

	/**
	 * Bonus for counting element type F.
	 */
	COUNTELEMENT_F,

	/**
	 * Bonus for counting element type Parchment.
	 */
	COUNTELEMENT_P,

	/**
	 * Bonus for direct points.
	 */
	DIRECTPOINT

}
