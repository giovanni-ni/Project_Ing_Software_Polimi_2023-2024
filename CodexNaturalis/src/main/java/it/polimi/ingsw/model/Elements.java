package it.polimi.ingsw.model;

/**
 * Represents the various elements that can be associated with a card.
 * These elements can appear in different positions on a card and are used to determine certain game mechanics.
 */
public enum Elements {
	MUSHROOMS,
	VEGETAL,
	ANIMALS,
	INSECT,
	HIDE,
	FEATHER,
	INK,
	PARCHMENT,
	EMPTY
}
