package it.polimi.ingsw.model;

/**
 * Enumeration of the 4 corner position of the card
 */
public enum CornerPosition {

	/**
	 * Up left position
	 */
	UPLEFT,

	/**
	 * Up right position
	 */
	UPRIGHT,

	/**
	 * Down left position
	 */
	DOWNLEFT,

	/**
	 * Down right position
	 */
	DOWNRIGHT;

}
