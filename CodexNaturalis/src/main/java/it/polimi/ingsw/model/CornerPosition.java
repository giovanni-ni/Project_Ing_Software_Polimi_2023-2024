package it.polimi.ingsw.model;

/**
 * Represents the positions of corners on a card.
 * Each enum constant corresponds to a specific corner position.
 */
public enum CornerPosition {

	/**
	 * Up left position
	 */
	UPLEFT,

	/**
	 * Up right position
	 */
	UPRIGHT,

	/**
	 * Down left position
	 */
	DOWNLEFT,

	/**
	 * Down right position
	 */
	DOWNRIGHT;

}
